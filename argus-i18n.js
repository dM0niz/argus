function setLang(lang) {
  document.documentElement.lang = lang === "pt" ? "pt-PT" : "en";
  document.getElementById("lang-pt").classList.toggle("active", lang === "pt");
  document.getElementById("lang-en").classList.toggle("active", lang === "en");
  document.querySelectorAll("[data-i18n]").forEach(function(el) {
    var k = el.getAttribute("data-i18n");
    var v = T[lang][k];
    if (!v) return;
    if (v.indexOf("<br>") !== -1) el.innerHTML = v;
    else el.textContent = v;
  });
}

function initReveal() {
  var io = new IntersectionObserver(function(entries) {
    entries.forEach(function(e) { if (e.isIntersecting) e.target.classList.add("on"); });
  }, { threshold: 0.12, rootMargin: "0px 0px -40px 0px" });
  document.querySelectorAll(".reveal").forEach(function(el) { io.observe(el); });
  var tl = document.getElementById("timeline");
  if (!tl) return;
  var steps = tl.querySelectorAll(".step");
  var tio = new IntersectionObserver(function(entries) {
    if (!entries[0].isIntersecting) return;
    steps.forEach(function(s, i) {
      setTimeout(function() { s.classList.add("on"); }, i * 120);
    });
    tio.disconnect();
  }, { threshold: 0.2 });
  tio.observe(tl);
}

function initParticles() {
  var c = document.getElementById("particles");
  if (!c || window.matchMedia("(prefers-reduced-motion: reduce)").matches) return;
  var ctx = c.getContext("2d"), pts = [], W, H, raf;
  function resize() {
    W = c.width = c.offsetWidth * devicePixelRatio;
    H = c.height = c.offsetHeight * devicePixelRatio;
    c.style.width = c.offsetWidth + "px";
    c.style.height = c.offsetHeight + "px";
    ctx.setTransform(devicePixelRatio, 0, 0, devicePixelRatio, 0, 0);
    pts = [];
    for (var i = 0; i < 100; i++) {
      pts.push({ x: Math.random() * c.offsetWidth, y: Math.random() * c.offsetHeight, r: Math.random() * 1.8 + 0.4, a: Math.random(), vx: (Math.random() - 0.5) * 0.15, vy: (Math.random() - 0.5) * 0.15 });
    }
  }
  function draw() {
    ctx.clearRect(0, 0, c.offsetWidth, c.offsetHeight);
    pts.forEach(function(p) {
      p.x += p.vx; p.y += p.vy;
      if (p.x < 0 || p.x > c.offsetWidth) p.vx *= -1;
      if (p.y < 0 || p.y > c.offsetHeight) p.vy *= -1;
      ctx.beginPath();
      ctx.arc(p.x, p.y, p.r, 0, Math.PI * 2);
      ctx.fillStyle = "rgba(147,197,253," + (0.25 + p.a * 0.45) + ")";
      ctx.fill();
    });
    for (var i = 0; i < pts.length; i++) {
      for (var j = i + 1; j < pts.length; j++) {
        var dx = pts[i].x - pts[j].x, dy = pts[i].y - pts[j].y, d = dx * dx + dy * dy;
        if (d < 9000) {
          ctx.strokeStyle = "rgba(96,165,250," + (0.06 * (1 - d / 9000)) + ")";
          ctx.beginPath(); ctx.moveTo(pts[i].x, pts[i].y); ctx.lineTo(pts[j].x, pts[j].y); ctx.stroke();
        }
      }
    }
    raf = requestAnimationFrame(draw);
  }
  resize(); draw();
  window.addEventListener("resize", resize);
}

